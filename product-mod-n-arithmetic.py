# Product Mod n Arithmetic
#
# Read an integer n and gives a .csv file with the product table of integers mod n
#
#
###############################################################################
###############################################################################

###############################################################################
################################## Modules ####################################
###############################################################################

import numpy as np

###############################################################################
# Main
###############################################################################

# Read the integer n
n = int(input('Type the integer n\n'))

# Create the matrix for the product table
mat = np.zeros((n,n), dtype=np.int)

# Calculate the product
for i in range(n):
	for j in range(n):
		mat[i][j] = (int(i)*int(j))%n

# Save the results
np.savetxt(fname='table_{}.csv'.format(str(n)), X=mat.astype(int), fmt ='%.0f')
